int[] GetRandomArray(int size, int minValue, int maxValue)
{
    int[] result = new int[size];
    for (int i = 0; i < size; i++)
    {
        result[i] = new Random().Next(minValue, maxValue + 1);
    }

    return result;
}

int CountEvenNumbers(int numbers){
    int count = 0;
    for (int i = 0; i < numbers; i++)
    {
        numbers % 2 == 0;
    }
    return count;
}